class Solution:
    def generateParenthesis(self, n: int) -> List[str]:
        path = []
        ans = []
        def dfs(i,left):
            if i == 2*n:
                if left == 0:
                    ans.append(''.join(path))
                return
            if left < n:
                path.append('(')
                dfs(i+1,left+1)
                path.pop()
            if left:
                path.append(')')
                dfs(i+1,left-1)
                path.pop()
        dfs(0,0)
        return ans
