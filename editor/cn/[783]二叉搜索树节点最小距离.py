# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def minDiffInBST(self, root: Optional[TreeNode]) -> int:
        res = float('inf')
        pre = float('-inf')

        def dfs(node):
            nonlocal res, pre
            if node:
                dfs(node.left)
                res = min(res, node.val - pre)
                pre = node.val
                dfs(node.right)

        dfs(root)
        return res
