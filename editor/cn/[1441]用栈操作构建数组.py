from typing import List


class Solution:
    def buildArray(self, target: List[int], n: int) -> List[str]:
        stack = []
        target_index = 0
        q = []
        for i in range(1,n+1):
            if target_index >= len(target):
                break
            q.append("Push")
            if target[target_index] == i:
                target_index += 1
                stack.append(i)
            else:
                q.append("Pop")
        return q



